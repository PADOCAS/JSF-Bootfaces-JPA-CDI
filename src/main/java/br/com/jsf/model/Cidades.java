package br.com.jsf.model;

import java.io.Serializable;
import java.util.Objects;
import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

/**
 *
 * @author lucia
 */
@Entity
public class Cidades implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    private String nome;

    //Muitas cidades para 1 Estado 
    //Carregar sempre o estado ao carregar a cidade... por isso tem o fetch
    //Qualquer alteração vai usar o cascade para gravação
    @ManyToOne(fetch = FetchType.EAGER, cascade = CascadeType.REFRESH)
    @org.hibernate.annotations.ForeignKey(name = "cidades_fk1")
    private Estados estados;

    public Cidades() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 37 * hash + Objects.hashCode(this.id);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Cidades other = (Cidades) obj;
        return Objects.equals(this.id, other.id);
    }

}
